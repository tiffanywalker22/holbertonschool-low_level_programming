#include "3-calc.h"
/**
 * op_add - add
 * @a: int 1
 * @b: int 2
 *
 * Return: the sum of as and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract
 * a: int 1
 * b: int 2
 *
 * Return: the difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply
 * @a: int 1
 * @b: int 2
 *
 * Return: multiple of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide
 * @a: int 1
 * b: int 2
 *
 * Return: division of a and b 
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus for remainder
 * @a: int 1
 * @b: int 2
 *
 * Return: mod of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
